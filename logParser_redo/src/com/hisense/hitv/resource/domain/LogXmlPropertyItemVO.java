package com.hisense.hitv.resource.domain;

import java.util.ArrayList;

/**
 * property节点相关属性
 * @author zhoudi
 * @version 1.0
 */
public class LogXmlPropertyItemVO {

    private String propertyId = null;
    private Integer propertyIndex = -1;
    private String propertyDefualt = null;
    private Integer propertyMaxLen = 0;
    private boolean propertyNullEnable = false;
    private ArrayList<LogXmlConvertVO> convertVOList = null;
    private ArrayList<LogXmlValidationVO> validationVOList = null;
    /**
     * @return the propertyId
     */
    public String getPropertyId() {
        return propertyId;
    }
    /**
     * @param propertyId the propertyId to set
     */
    public void setPropertyId(String propertyId) {
        this.propertyId = propertyId;
    }
    /**
     * @return the propertyIndex
     */
    public Integer getPropertyIndex() {
        return propertyIndex;
    }
    /**
     * @param propertyIndex the propertyIndex to set
     */
    public void setPropertyIndex(Integer propertyIndex) {
        this.propertyIndex = propertyIndex;
    }
    /**
     * @return the propertyDefualt
     */
    public String getPropertyDefualt() {
        return propertyDefualt;
    }
    /**
     * @param propertyDefualt the propertyDefualt to set
     */
    public void setPropertyDefualt(String propertyDefualt) {
        this.propertyDefualt = propertyDefualt;
    }
    /**
     * @param convertVOList the convertVOList to set
     */
    public void setConvertVOList(ArrayList<LogXmlConvertVO> convertVOList) {
        this.convertVOList = convertVOList;
    }
    /**
     * @return the convertVOList
     */
    public ArrayList<LogXmlConvertVO> getConvertVOList() {
        return convertVOList;
    }
    /**
     * @param propertyMaxLen the propertyMaxLen to set
     */
    public void setPropertyMaxLen(Integer propertyMaxLen) {
        this.propertyMaxLen = propertyMaxLen;
    }
    /**
     * @return the propertyMaxLen
     */
    public Integer getPropertyMaxLen() {
        return propertyMaxLen;
    }
    /**
     * @return the PropertyNullEnable
     */
    public boolean getPropertyNullEnable() {
        return propertyNullEnable;
    }
    /**
     * @param propertyNullEnable the PropertyNullEnable to set
     */
    public void setPropertyNullEnable(boolean propertyNullEnable) {
        this.propertyNullEnable = propertyNullEnable;
    }
    /**
     * @return the validationVOList
     */
    public ArrayList<LogXmlValidationVO> getValidationVOList() {
        return validationVOList;
    }
    /**
     * @param validationVOList the validationVOList to set
     */
    public void setValidationVOList(ArrayList<LogXmlValidationVO> validationVOList) {
        this.validationVOList = validationVOList;
    }
}
